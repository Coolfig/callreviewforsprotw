/** Auto-calculate the season year from a play date string and league */
export function getSeasonYear(dateStr: string, league: string): number {
  // Try to parse a real date; fall back to extracting a 4-digit year
  const parsed = new Date(dateStr);
  const year = !isNaN(parsed.getTime()) ? parsed.getFullYear() : parseInt(dateStr.match(/\d{4}/)?.[0] || "2024", 10);
  const month = !isNaN(parsed.getTime()) ? parsed.getMonth() + 1 : 1; // Jan = 1

  // MLB runs March–Oct → same calendar year
  if (league === "MLB") return year;

  // NFL runs Sep–Feb, NBA Oct–Jun, NHL Oct–Jun → use starting year
  if (month >= 3 && month <= 6) return year - 1; // spring half of prior season
  if (month <= 2) return year - 1; // Jan/Feb is prior season
  return year; // Sep–Dec is current season start
}

